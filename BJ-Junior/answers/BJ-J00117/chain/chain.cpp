#include<bits/stdc++.h>
using namespace std;
int t,n,k,q;
int s[1005][1005];
int l;
int main(){
    cin>>t;
    for(int i=1;i<=t;i++){
        cin>>n>>k>>q;
        for(int j=1;j<=n;j++){
            cin>>l;
            for(int z=1;z<=l;z++){
                cin>>s[j][z];
            }
        }
        for(int j=1;j<=q;j++){
            int r,c;
            cin>>r>>c;
        }
    }
    return 0;
}


