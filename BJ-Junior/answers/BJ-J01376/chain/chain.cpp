int main(){
    freopen("chain.in", "r", stdin);
    freopen("chain.out", "w", stdout);
    int t;
    scanf("%d", &t);
    for(int i = 0; i <= t; ++i){
        int n, k, q; scanf("%d %d %d", &n, &k, &q);
        for(int j = 0; j < n; ++j){
            int l; scanf("%d ", &l);
            for(int k = 0; k < l; ++k){
                int p; scanf("%d", &p);
            }
        }
        for(int j = 0; j < q; ++j){
            int r, c; scanf("%d %d", &r, &c);
            printf("0");
        }
    }
    return 0;
}
