#include<bits/stdc++.h>
using namespace std;
int a[4][13],n=0;
string m;
int main(){
    freopen("zhipai.in","r",stdin);
    freopen("zhipai.out","w",stdout);
    while(cin>>m){
        if(m[0]=='D'){
            if(m[1]>='2'&&m[1]<='9'){
                a[1][m[1]-'0']++;
            }
            else{
                if(m[1]=='T'){
                    a[1][10]++;
                }
                if(m[1]=='J'){
                    a[1][11]++;
                }
                if(m[1]=='Q'){
                    a[1][12]++;
                }
                if(m[1]=='K'){
                    a[1][13]++;
                }
                if(m[1]=='A'){
                    a[1][1]++;
                }
            }
        }
        else if(m[0]=='C'){
            if(m[1]>='2'&&m[1]<='9'){
                a[2][m[1]-'0']++;
            }
            else{
                if(m[1]=='T'){
                    a[2][10]++;
                }
                if(m[1]=='J'){
                    a[2][11]++;
                }
                if(m[1]=='Q'){
                    a[2][12]++;
                }
                if(m[1]=='K'){
                    a[2][13]++;
                }
                if(m[1]=='A'){
                    a[2][1]++;
                }
            }
        }
        else if(m[0]=='H'){
            if(m[1]>='2'&&m[1]<='9'){
                a[3][m[1]-'0']++;
            }
            else{
                if(m[1]=='T'){
                    a[3][10]++;
                }
                if(m[1]=='J'){
                    a[3][11]++;
                }
                if(m[1]=='Q'){
                    a[3][12]++;
                }
                if(m[1]=='K'){
                    a[3][13]++;
                }
                if(m[1]=='A'){
                    a[3][1]++;
                }
            }
        }
        if(m[0]=='S'){
            if(m[1]>='2'&&m[1]<='9'){
                a[0][m[1]-'0']++;
            }
            else{
                if(m[1]=='T'){
                    a[0][10]++;
                }
                if(m[1]=='J'){
                    a[0][11]++;
                }
                if(m[1]=='Q'){
                    a[0][12]++;
                }
                if(m[1]=='K'){
                    a[0][13]++;
                }
                if(m[1]=='A'){
                    a[0][1]++;
                }
            }
        }
    }
    for(int i=0;i<4;i++){
        for(int j=1;j<=13;j++){
            if(!a[i][j]){
                n++;
            }
        }
    }
    cout<<n+1;
    return 0;
}
