#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
ll t;
int main(){
	freopen("chain.in","r",stdin);
	freopen("chain.out","w",stdout);
	scanf("%lld",&t);
	while(t--){
		ll n,k,q,l;
		scanf("%lld%lld%lld",&n,&k,&q);
		for(ll i=1;i<=n;i++){
			scanf("%lld",&l);
			for(ll j=1;j<=l;j++){
				ll temp;
				scanf("%lld",&temp);
			}
		}
		while(q--){
			ll t1,t2;
			scanf("%lld%lld",&t1,&t2);
		}
		while(q--){
			cout<<"1\n";
		}
	}
	return 0;
}
