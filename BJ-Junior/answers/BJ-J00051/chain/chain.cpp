#include <bits/stdc++.h>
using namespace std;
#define ll long long
ll n, k, q;
int main(){
    freopen("chain.in", "r", stdin);
    freopen("chain.out", "w", stdout);
    int T;
    cin >> T;
    while(T--){
        cin >> n >> k >> q;
        for(int i = 1; i <= q; i++){
            cout << "0\n";
        }
    }
    fclose(stdin);
    fclose(stdout);
    return 0;
}
