int t;
int main(){
    freopen("chain.in","r",stdin);
    freopen("chain.out","w",stdout);
    scanf("%d",&t);
    for(int i=1;i<=t;i++){
        int n,k,q;
        scanf("%d%d%d",&n,&k,&q);
        for(int j=1;j<=n;j++){
            int a;
            scanf("%d",&a);
        }
        for(int j=1;j<=q;j++){
            int r,c;
            scanf("%d%d",&r,&c);
        }
        for(int j=1;j<=q;j++){
            printf("0\n");
        }
    }
}
