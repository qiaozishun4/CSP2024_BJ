#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define LL __int128

int main() {
    freopen("chain.in", "r", stdin);
    freopen("chain.out", "w", stdout);

    int T, n, k, Q;
    cin >> T;
    while(T--) {
        cin >> n >> k >> Q;
        for(int i = 1; i <= n; i++) {
            int a; cin >> a;
            while(a--) {
                int b; cin >> b;
            }
        }
        for(int i = 1; i <= Q; i++) {
            int a, b; cin >> a >> b;
        }
        while(Q--) cout << 1 << "\n";
    }
    return 0;
}

